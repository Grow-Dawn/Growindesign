   <p>    
         The runners will enter Fisherman's Wharf from the west end.  They will pass many sightseeing places as they finish their run. After a quick change the participants will start their 3 mile long swim from Fisherman's Wharf out to Alcatraz Island and back to Fishermans Wharf for another quick change for the last leg of the journey on bike.
         <br><br>Fisherman's Wharf is a great place for families and spectators to spend some down time or for the participants to come to after the race to relax.  Some of the many sites include the wharf itself with many local shops and restaurants, Pier 39, Aquarium of the Bay, Telegraph Hill, and the clock tower at the Ferry Plaza. Not to be missed are the sea lions, the fresh fish market, the trolley tours and bay tours.
