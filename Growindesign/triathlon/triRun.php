   <p>    
         The race will start with a 16 mile run from San Quentin Prison to Fisherman’s Wharf.  The runners will head along Sir Francis Drake Blvd over to Highway 101.  As the route travels south along the western side of the Richardson Bay the runner can glimpse spots of the bay off to the left and the gentle mountains off to the right. Further along near Sausalito the runners go through the tunnel and as they emerge they start to get their first site of the Golden Gate Bridge. For those coming along as spectators and supporters of the runners, don’t forget to bring some change for the bridge tolls.  When they leave the bridge, they will head towards Fisherman’s Wharf and the second leg of the triathlon.
         <br><br>Not to miss out on the attractions.  Spectators may enjoy the Golden Gate, the Golden Gate Park with many things to do, The Presidio military Base where they also filmed the movie with the same name, and Seaplane Adventures.
         
