   <br><strong>Image Formats<br></strong>
   What is the right format to use?<br>
   There are only three image formats for the web: GIF, JPG, and PNG. And they each have a specific purpose. <br>
   ~	GIF—Use the GIF format for flat color images. These are images with just a few colors.<br>
   ~	JPG—Use the JPG format for photographic images. These are images with millions of colors.<br>
   ~	PNG—Use the PNG format if you don't need your images to display on mobile devices. They are good for both flat color and photographic images. It's best to save your images as both PNG and either JPG or GIF and then use the version that is smaller.<br>
   <strong><br>Image Dimensions<br></strong>
   What are the image dimensions?<br>
   Most cameras take photos that are bigger than the average web page can display.  In Photoshop or another photo program, change the dimensions to somewhere around 500 x 500 pixels or smaller.  The web prefers a smaller resolution too, about 72 pixels. <br>
   <strong><br>Cropped Images<br></strong>
   Should I crop it?<br>
   Crop the image as tightly as you can. The more you cut off the image the smaller it will be. Cropping helps focus on the subject of the image by removing unnecessary backgrounds. <br>
   <strong><br>Colors in the GIF Image<br></strong>
   How many colors does a GIF image use?<br>
   GIFs are flat color images that include an index of the colors present in the image.  A GIF index can include more colors than are actually displayed. Reducing the index to only the colors in the image can also reduce the file size. <br>
   <strong><br>Screen Resolution<br></strong>
   What is the most common resolution?<br>
   Approximately 85% of web-users as of 2009 have a screen resolution of at least 1024 x 768 pixels but there are still users who are on computers with 800 x 600 as well as those who use tablets and smart phones.<br>
