   <p>    
         For the last leg of the race the participants will perform a 25 mile cycling course from Fishermans Wharf back to San Quentin Prison.  The participants will access the Dwight D Eisenhower highway onto the San Francisco-Oakland Bay Bridge which cuts across the Yerba Buena Island with a quaint tunnel.  This bridge has two levels, one for each direction.  Again, make sure all participants have spare change for the tolls.  The road will curve around and change to the John T. Knox freeway.  Once they cross the Richmond-San Rafael Bridge the race is almost over with only a short jaunt on Sir Francis Drake Blvd to the finish.
         <br><br>Not to miss on this stretch is Baker Beach, the Red Oak Victory Ship at Point Potrero, Rosie the Riveter Visitor Center, Berkley Fields and the University of California at Berkley.  There are many small islands out in the bay to see and many other sites to see.
         
